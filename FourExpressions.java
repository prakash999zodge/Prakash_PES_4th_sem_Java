class FourExpressions
{
	public static void main(String[] args)
	{
		System.out.println("a. - 5 + 8 * 6\nAnswer : "+(- 5 + 8 * 6));
		System.out.println("b. (55 + 9) % 9\nAnswer : "+((55 + 9) % 9));
		System.out.println("c. 20 + - 3 * 5 / 8\nAnswer : "+(20 + - 3 * 5 / 8));
		System.out.println("d. 5 + 15 / 3 * 2 - 8 % 3\nAnswer : "+(5 + 15 / 3 * 2 - 8 % 3));
	}
}
